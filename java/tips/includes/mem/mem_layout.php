    <summary>Disposition de la mémoire</summary>

    <div>
        <p>
            <strong>1 bit</strong> correspond à un chiffre binaire: soit
            <code>0</code>, soit <code>1</code>.
        </p>

        <div class="mem spaced">
            <div class="visual">
                <div class="bit bit-enabled">0 / 1</div>
            </div>
        </div>
    </div>

    <p>
        Les bits sont combinés pour former des octets (8 bits), qui
        sont ensuite utilisés pour coder des caractères, des
        nombres, etc., dans un ordinateur. L'ordre de lecture des
        bits se fait de la droite vers la gauche.
    </p>

