package exo.basics.primitive_types;

public class DataType
{
	public static void main(String[] args)
	{
		byte age = 25;
		double price = 199.99D;
		boolean isLampOn = true;
		char note = 'B';
		int distanceMoon = 384400;
		long worldPopulation = 8000000000L;
		float ovenTemperature = 220.5F;

		System.out.println(age);
		System.out.println(price);
		System.out.println(isLampOn);
		System.out.println(note);
		System.out.println(distanceMoon);
		System.out.println(worldPopulation);
		System.out.println(ovenTemperature);
	}
}
